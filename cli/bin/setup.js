#!/usr/bin/env node
import { input, password, select, checkbox, confirm } from '@inquirer/prompts';
import { execSync, spawn } from 'child_process';
import { existsSync, readFileSync, writeFileSync, rmSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { randomBytes } from 'crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
const TEMPLATES_DIR = join(__dirname, '..', 'templates');
const TARGET_DIR = process.cwd();
const PKG_VERSION = JSON.parse(
  readFileSync(join(__dirname, '..', 'package.json'), 'utf8')
).version;
const USE_LOCAL_IMAGE = process.argv.includes('--local-image');
const APP_IMAGE = USE_LOCAL_IMAGE ? 'open-daimon:local' : `ghcr.io/ngirchev/open-daimon:${PKG_VERSION}`;

function checkCommand(cmd) {
  try {
    execSync(`${cmd} --version`, { stdio: 'ignore' });
    return true;
  } catch {
    return false;
  }
}

async function checkOllama(url) {
  try {
    const res = await fetch(url, { signal: AbortSignal.timeout(3000) });
    return res.status < 500;
  } catch {
    return false;
  }
}

function pullOllamaModel(model) {
  return new Promise((resolve) => {
    console.log(`  Pulling ${model} (this may take a few minutes)...`);
    const proc = spawn('ollama', ['pull', model], { stdio: 'inherit' });
    proc.on('close', resolve);
    proc.on('error', () => {
      console.log(`  ollama CLI not found — pull manually: ollama pull ${model}`);
      resolve();
    });
  });
}

function spawnAsync(cmd, args, cwd) {
  return new Promise((resolve) => {
    const proc = spawn(cmd, args, { stdio: 'inherit', cwd });
    proc.on('close', resolve);
  });
}

async function waitForApp(url, timeoutMs = 120000) {
  const start = Date.now();
  process.stdout.write('\nWaiting for app to be ready');
  while (Date.now() - start < timeoutMs) {
    try {
      const res = await fetch(url, { signal: AbortSignal.timeout(3000) });
      if (res.ok) {
        console.log(' ✓ Ready!');
        return true;
      }
    } catch {
      // not ready yet
    }
    process.stdout.write('.');
    await new Promise((r) => setTimeout(r, 3000));
  }
  console.log(' ✗ Timed out after 2 minutes.');
  console.log('  Check logs: docker compose logs -f opendaimon-app');
  return false;
}

function genPassword() {
  return randomBytes(16).toString('hex');
}

function parseEnv(filePath) {
  if (!existsSync(filePath)) return {};
  const result = {};
  for (const line of readFileSync(filePath, 'utf8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx === -1) continue;
    result[trimmed.slice(0, idx).trim()] = trimmed.slice(idx + 1).trim();
  }
  return result;
}

function buildEnv(cfg) {
  const profiles = cfg.services.join(',');
  return [
    '# Generated by @ngirchev/open-daimon setup wizard',
    '# Do NOT commit this file to git',
    '',
    '# Telegram',
    `TELEGRAM_USERNAME=${cfg.telegramUsername}`,
    `TELEGRAM_TOKEN=${cfg.telegramToken}`,
    'TELEGRAM_CHANNEL_ID=',
    '',
    '# Admin',
    `ADMIN_TELEGRAM_ID=${cfg.adminId}`,
    `ADMIN_REST_EMAIL=${cfg.adminRestEmail}`,
    '',
    '# AI',
    `OPENROUTER_KEY=${(cfg.provider === 'openrouter' || cfg.provider === 'both') ? cfg.openrouterKey : ''}`,
    `SERPER_KEY=${cfg.serperKey}`,
    `OLLAMA_BASE_URL=${((cfg.provider === 'ollama' || cfg.provider === 'both') ? cfg.ollamaUrl : 'http://localhost:11434').replace('localhost', 'host.docker.internal').replace('127.0.0.1', 'host.docker.internal')}`,
    '',
    '# Database',
    'POSTGRES_HOST=localhost',
    'POSTGRES_PORT=5432',
    'POSTGRES_DB=opendaimon',
    'POSTGRES_USER=postgres',
    `POSTGRES_PASSWORD=${cfg.dbPassword}`,
    `SPRING_DATASOURCE_USERNAME=postgres`,
    `SPRING_DATASOURCE_PASSWORD=${cfg.dbPassword}`,
    '',
    '# User Access Configuration (Telegram)',
    `TELEGRAM_ACCESS_ADMIN_IDS=${cfg.adminId}`,
    'TELEGRAM_ACCESS_ADMIN_CHANNELS=',
    'TELEGRAM_ACCESS_VIP_IDS=',
    'TELEGRAM_ACCESS_VIP_CHANNELS=',
    'TELEGRAM_ACCESS_REGULAR_IDS=',
    'TELEGRAM_ACCESS_REGULAR_CHANNELS=',
    '',
    '# User Access Configuration (REST)',
    `REST_ACCESS_ADMIN_EMAILS=${cfg.adminRestEmail}`,
    'REST_ACCESS_VIP_EMAILS=',
    'REST_ACCESS_REGULAR_EMAILS=',
    '',
    '# Spring profiles (simple = load application-simple.yml created by wizard)',
    `SPRING_PROFILES_ACTIVE=${['simple', ...(cfg.services.includes('logging') ? ['logging'] : [])].join(',')}`,
    '',
    '# Active Docker Compose service profiles (comma-separated)',
    '# Available: monitoring, logging, storage',
    `COMPOSE_PROFILES=${profiles}`,
    '',
  ].join('\n');
}

async function main() {
  console.log('');
  console.log('╔══════════════════════════════════════════╗');
  console.log('║        OpenDaimon Setup Wizard           ║');
  console.log('╚══════════════════════════════════════════╝');
  console.log('');

  if (!checkCommand('docker')) {
    console.error('Docker not found. Install Docker Desktop: https://www.docker.com/products/docker-desktop/');
    process.exit(1);
  }
  console.log('Docker found.');

  const envPath = join(TARGET_DIR, '.env');
  const prev = parseEnv(envPath);
  const isUpdate = Object.keys(prev).length > 0;

  if (isUpdate) {
    console.log('Found existing config — pre-filling values. Press Enter to keep each value.\n');
  }

  // helper: mask secret for display (show first 4 chars + ***)
  const mask = (v) => (v ? v.slice(0, 4) + '***' : '');

  // ── Telegram ──────────────────────────────────────────────────────────────
  console.log('\n── Telegram ──────────────────────────────────');
  console.log('   See docs at https://github.com/NGirchev/open-daimon/blob/master/docs/setup-telegram.md');
  console.log('');

  const telegramTokenRaw = await password({
    message: prev.TELEGRAM_TOKEN
      ? `Bot token (leave blank to keep ${mask(prev.TELEGRAM_TOKEN)}):`
      : 'Bot token (from @BotFather):',
    validate: (v) => v.trim().length > 0 || !!prev.TELEGRAM_TOKEN || 'Required',
  });
  const telegramToken = telegramTokenRaw.trim() || prev.TELEGRAM_TOKEN || '';

  const telegramUsername = await input({
    message: 'Bot username (without @, e.g. my_ai_bot):',
    default: prev.TELEGRAM_USERNAME || '',
    validate: (v) => v.trim().length > 0 || 'Required',
  });

  const adminId = await input({
    message: 'Your Telegram user ID (send any message to @userinfobot):',
    default: prev.ADMIN_TELEGRAM_ID || '',
    validate: (v) => /^\d+$/.test(v.trim()) || 'Must be a number',
  });

  // ── Web UI Access ─────────────────────────────────────────────────────────
  console.log('\n── Web UI Access ─────────────────────────────');

  const adminRestEmail = await input({
    message: 'Your email for web UI admin access:',
    default: prev.ADMIN_REST_EMAIL || '',
    validate: (v) => /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(v.trim()) || 'Enter a valid email',
  });

  // ── AI Provider ───────────────────────────────────────────────────────────
  console.log('\n── AI Provider ───────────────────────────────');

  const prevProvider = (prev.OPENROUTER_KEY && prev.OLLAMA_BASE_URL)
    ? 'both'
    : prev.OPENROUTER_KEY ? 'openrouter'
    : prev.OLLAMA_BASE_URL ? 'ollama'
    : null;

  const provider = await select({
    message: 'Choose AI provider:',
    default: prevProvider ?? 'openrouter',
    choices: [
      {
        name: 'OpenRouter (cloud — free models available, no GPU needed)',
        value: 'openrouter',
      },
      {
        name: 'Ollama (local — run models on your machine, no API key)',
        value: 'ollama',
      },
      {
        name: 'Both — OpenRouter for cloud models + Ollama for local models',
        value: 'both',
      },
    ],
  });

  let openrouterKey = '';
  let ollamaUrl = 'http://localhost:11434';

  if (provider === 'openrouter' || provider === 'both') {
    if (provider === 'both') console.log('   ── OpenRouter (cloud) ─────────────────────');
    console.log('   Get a free API key at https://openrouter.ai/keys');
    const openrouterKeyRaw = await password({
      message: prev.OPENROUTER_KEY
        ? `OpenRouter API key (leave blank to keep ${mask(prev.OPENROUTER_KEY)}):`
        : 'OpenRouter API key:',
      validate: (v) => v.trim().length > 0 || !!prev.OPENROUTER_KEY || 'Required',
    });
    openrouterKey = openrouterKeyRaw.trim() || prev.OPENROUTER_KEY || '';
  }

  if (provider === 'ollama' || provider === 'both') {
    if (provider === 'both') console.log('   ── Ollama (local) ──────────────────────────');
    console.log('   Make sure Ollama is running: ollama serve');
    // Reverse the host.docker.internal→localhost transform stored in .env so we check from the host
    const prevOllamaUrl = (prev.OLLAMA_BASE_URL || '')
      .replace('host.docker.internal', 'localhost');
    ollamaUrl = await input({
      message: 'Ollama base URL:',
      default: prevOllamaUrl || 'http://localhost:11434',
    });

    process.stdout.write('   Checking Ollama connection... ');
    const ollamaAlive = await checkOllama(ollamaUrl.trim());
    if (ollamaAlive) {
      console.log('OK');
      const doPullModel = await confirm({
        message: 'Pull default model qwen2.5:3b now? (~1.9 GB, supports tool calling)',
        default: true,
      });
      if (doPullModel) {
        await pullOllamaModel('qwen2.5:3b');
      }
    } else {
      console.log('UNREACHABLE');
      console.log('   ⚠  Ollama is not running at ' + ollamaUrl.trim());
      console.log('   Start it with: ollama serve');
      console.log('   Then manually pull the model: ollama pull qwen2.5:3b');
      const continueAnyway = await confirm({
        message: 'Continue setup anyway?',
        default: true,
      });
      if (!continueAnyway) process.exit(0);
    }
  }

  // ── Web Search (optional) ─────────────────────────────────────────────────
  console.log('\n── Web Search (optional) ─────────────────────');
  const wantSerper = await confirm({
    message: 'Add Serper API key for web search?',
    default: !!prev.SERPER_KEY,
  });
  let serperKey = prev.SERPER_KEY || '';
  if (wantSerper) {
    console.log('   Get a free key (2500 searches) at https://serper.dev');
    const serperKeyRaw = await password({
      message: prev.SERPER_KEY
        ? `Serper API key (leave blank to keep ${mask(prev.SERPER_KEY)}):`
        : 'Serper API key:',
    });
    serperKey = serperKeyRaw.trim() || prev.SERPER_KEY || '';
  }

  // ── Optional Services ─────────────────────────────────────────────────────
  console.log('\n── Optional Services ─────────────────────────');
  const prevProfiles = prev.COMPOSE_PROFILES ? prev.COMPOSE_PROFILES.split(',').filter(Boolean) : [];
  const services = await checkbox({
    message: 'Which optional services to start?',
    choices: [
      { name: 'Storage — MinIO for image/file uploads', value: 'storage', checked: prevProfiles.includes('storage') || !isUpdate },
      { name: 'Monitoring — Prometheus + Grafana', value: 'monitoring', checked: prevProfiles.includes('monitoring') },
      { name: 'Logging — Elasticsearch + Kibana (~2 GB RAM)', value: 'logging', checked: prevProfiles.includes('logging') },
    ],
  });

  // ── Database ──────────────────────────────────────────────────────────────
  console.log('\n── Database ──────────────────────────────────');
  let dbPassword = await input({
    message: prev.POSTGRES_PASSWORD
      ? `PostgreSQL password (leave blank to keep existing):`
      : 'PostgreSQL password (leave blank to auto-generate):',
    default: '',
  });
  if (!dbPassword.trim()) {
    dbPassword = prev.POSTGRES_PASSWORD || genPassword();
    if (!prev.POSTGRES_PASSWORD) console.log(`   Generated password: ${dbPassword}`);
  }

  const cfg = {
    telegramToken: telegramToken.trim(),
    telegramUsername: telegramUsername.trim(),
    adminId: adminId.trim(),
    adminRestEmail: adminRestEmail.trim(),
    provider,
    openrouterKey: openrouterKey.trim(),
    ollamaUrl: ollamaUrl.trim(),
    serperKey: serperKey.trim(),
    services,
    dbPassword: dbPassword.trim(),
  };

  // ── Write files ───────────────────────────────────────────────────────────
  console.log('\n── Writing files ─────────────────────────────');

  writeFileSync(envPath, buildEnv(cfg), 'utf8');
  console.log('  .env');
  if ((cfg.provider === 'ollama' || cfg.provider === 'both') && /localhost|127\.0\.0\.1/.test(cfg.ollamaUrl)) {
    console.log(`  ⚠  Ollama URL contains localhost — saved as host.docker.internal in .env so the container can reach the host`);
  }

  let composeTemplate = readFileSync(join(TEMPLATES_DIR, 'docker-compose.yml'), 'utf8')
    .replace('ghcr.io/ngirchev/open-daimon:latest', APP_IMAGE);
  if (USE_LOCAL_IMAGE) {
    composeTemplate = composeTemplate.replace(
      `image: ${APP_IMAGE}`,
      `image: ${APP_IMAGE}\n    pull_policy: never`
    );
  }
  writeFileSync(join(TARGET_DIR, 'docker-compose.yml'), composeTemplate, 'utf8');
  console.log('  docker-compose.yml');

  // Copy the provider-specific template to application-simple.yml
  const appSimpleYmlPath = join(TARGET_DIR, 'application-simple.yml');
  if (existsSync(appSimpleYmlPath) && statSync(appSimpleYmlPath).isDirectory()) {
    rmSync(appSimpleYmlPath, { recursive: true });
  }
  let providerTemplate = readFileSync(join(TEMPLATES_DIR, `application-simple-${cfg.provider}.yml`), 'utf8');
  if (services.includes('storage')) {
    // Enable storage and file-upload when user chose to run MinIO
    providerTemplate = providerTemplate
      .replace(/^(    storage:\n      enabled:) false/m, '$1 true')
      .replace(/^(    file-upload:\n      enabled:) false/m, '$1 true');
  }
  writeFileSync(appSimpleYmlPath, providerTemplate, 'utf8');
  console.log('  application-simple.yml');

  if (services.includes('monitoring')) {
    const prometheusTemplate = readFileSync(join(TEMPLATES_DIR, 'prometheus.yml'), 'utf8');
    writeFileSync(join(TARGET_DIR, 'prometheus.yml'), prometheusTemplate, 'utf8');
    console.log('  prometheus.yml');
  }

  if (services.includes('logging')) {
    const logstashConf = readFileSync(join(TEMPLATES_DIR, 'logstash.conf'), 'utf8');
    writeFileSync(join(TARGET_DIR, 'logstash.conf'), logstashConf, 'utf8');
    console.log('  logstash.conf');
  }

  const exampleTemplate = readFileSync(join(TEMPLATES_DIR, 'application-simple.yml.example'), 'utf8');
  writeFileSync(join(TARGET_DIR, 'application-simple.yml.example'), exampleTemplate, 'utf8');
  console.log('  application-simple.yml.example');

  // ── Start the stack ───────────────────────────────────────────────────────
  console.log('');
  const composeCmd = checkCommand('docker compose') ? ['docker', 'compose'] : ['docker-compose'];

  const doStart = await confirm({
    message: 'Start the stack now? (docker compose up -d)',
    default: true,
  });
  if (doStart) {
    console.log('\nDownloading images (this may take a few minutes on first run)...');
    await spawnAsync(composeCmd[0], [...composeCmd.slice(1), 'pull'], TARGET_DIR);

    console.log('\nStarting containers...');
    await spawnAsync(composeCmd[0], [...composeCmd.slice(1), 'up', '-d'], TARGET_DIR);

    console.log('\nContainer status:');
    await spawnAsync(composeCmd[0], [...composeCmd.slice(1), 'ps'], TARGET_DIR);

    await waitForApp('http://localhost:8080/actuator/health');
  }

  // ── Summary ───────────────────────────────────────────────────────────────
  console.log('');
  console.log('╔══════════════════════════════════════════╗');
  console.log('║            Setup complete!               ║');
  console.log('╚══════════════════════════════════════════╝');
  console.log('');
  if (!doStart) {
    console.log('Next steps:');
    console.log('');
    console.log('  1. Review generated files:');
    console.log('       .env                      — secrets and credentials');
    console.log('       docker-compose.yml         — service definitions');
    console.log('       application-simple.yml     — AI provider config (edit as needed)');
    console.log('       application-simple.yml.example — all available options');
    console.log('');
    console.log('  2. Start the stack:');
    console.log('       docker compose up -d');
    console.log('');
  } else {
    console.log('The stack is starting. To check logs:');
    console.log('');
  }
  console.log('  Watch logs:');
  console.log('       docker compose logs -f opendaimon-app');
  console.log('');
  console.log('  ── URLs ──────────────────────────────────────');
  console.log('  App:        http://localhost:8080');
  console.log('  Swagger UI: http://localhost:8080/swagger-ui/index.html');
  if (services.includes('monitoring')) {
    console.log('  Grafana:    http://localhost:3000  (admin / admin123456)');
    console.log('  Prometheus: http://localhost:9090');
  }
  if (services.includes('logging')) {
    console.log('  Kibana:     http://localhost:5601');
  }
  if (services.includes('storage')) {
    console.log('  MinIO:      http://localhost:9001  (minioadmin / minioadmin)');
  }
  console.log('');
}

main()
  .then(() => process.exit(0))
  .catch((err) => {
    if (err?.name === 'ExitPromptError') {
      console.log('\nSetup cancelled.');
    } else {
      console.error('\nError:', err?.message ?? err);
      process.exit(1);
    }
  });
